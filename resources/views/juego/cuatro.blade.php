            <h1 class="text-white text-center text-justify">Resumen del argumento</h1>
            </p>
            <p class="text-white text-justify">
                El argumento de Gears of War 4 comienza en una aldea alejada de la civilización, donde habitan
                supervivientes conocidos como Forasteros. Esta nueva facción se opone al nuevo régimen de la resurgida
                Coalición de Gobiernos Ordenados de privar a las personas de su libertad en ciudades amuralladas pero
                seguras de los violentos fenómenos naturales generados por el planeta Sera en consecuencia de la
                activación del Arma Desmanteladora de Imulsión dos décadas antes.
            </p>
            <p class="text-white  text-justify">
                James Dominic Fenix, el protagonista de Gears 4, junto con su mejor amigo, Delmont Walker, ambos
                anteriormente pertenecientes a las fuerzas armadas de la CGO, buscan asilo en una aldea Forastera
                después de un incidente clasificado que hizo que desertaran del gobierno al que pertenecían. Reyna Díaz,
                la líder de los Forasteros, permite a JD y a Del quedarse después de que ellos advirtieran de un ataque
                de la CGO hacia su población en respuesta a una intrusión que habían realizado previamente.
            </p>
            <p class="text-white  text-justify">
                Una vez parte de los Forasteros, los desertores conocen a Kait Díaz, la hija de la líder, y a Oscar
                Díaz, el cuñado de Reyna. Durante su estancia en la aldea, secuestros inexplicables se suscitan, siendo
                Reyna Díaz una de las víctimas. Los únicos sobrevivientes al rapto son JD, Del, Kait y Oscar, quienes se
                determinan a ir en busca de los raptados y de los culpables. Para ello se adentran en los inexplorados
                bosques aledaños a la aldea. Más temprano que tarde, Oscar es también abducido, dejando al trío
                desamparado.
            </p>
            <p class="text-white text-justify">
