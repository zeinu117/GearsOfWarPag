                Los personajes continúan su búsqueda y eventualmente llegan a un antiguo fuerte abandonado. Estando
                dentro, encuentran algo sorprendente: extrañas vainas orgánicas del tamaño de una persona. Al abrir una,
                encuentran restos de algo que parece ser humano.

                Pronto encuentran lo que las vainas engendran: veloces, pálidas y hostiles criaturas bípedas que pronto
                acorralan al equipo. Afortunadamente, son capaces de resistir el ataque. Los Forasteros nombran a estas
                criaturas como Juvies. Después de determinado tiempo, los Juvies evolucionan en Drones, unidades más
                fuertes y grandes que los humanos que rápidamente aprenden a usar armas y estrategias de combate.
