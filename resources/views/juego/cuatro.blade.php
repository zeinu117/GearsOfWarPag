                Han pasado 25 años desde que el Arma Desmanteladora de Imulsión creada por Adam Jonathon Fenix fuera
                utilizada con el fin de erradicar la Pandemia Lambent, a las hordas de los Locust y terminara por
                evaporar toda la Imulsión presente en el planeta Sera. Tras este evento, la humanidad fue forzada a
                buscar nuevas formas de supervivencia después de tener que dejar de depender de la Imulsión, el
                combustible fósil del planeta el cual prácticamente dejó de existir. Pero a consecuencia del uso del
                arma desmanteladora de Imulsión, el planeta entró en un estado de cataclismo climático en la forma de
                poderosos vendavales conocidos simplemente como "Llamaradas" obligando a la reformada Coalición de
                Gobiernos Ordenados a construir Asentamientos: ciudades amuralladas creadas para contener las fuertes
                corrientes de viento para proteger a los pocos humanos que quedaron tras la Guerra contra los Locust,
                declarando la aplicación de la Ley Marcial a todo aquel que abandone las ciudades amuralladas sin razón
                alguna. Esto provocó el descontento de algunos sobrevivientes quienes escogieron vivir fuera de las
                ciudades amuralladas siendo identificados como "Forasteros" los cuales viven fuera de la jurisdicción de
                la CGO, atacando las ciudades amuralladas para obtener recursos.
